static struct font_t font={ "Default",8,8,font1};

void blitChar(NCCAPixmap p,unsigned char c, int x, int y,NCCAPixel col)
{
int dx,dy;
unsigned char *ptr=font.data+(font.width*font.height/8)*c;
for(dy=0;dy<font.height;dy++)
	{
	int byte;
	for(byte=0;byte<font.width/8;byte++)
		{
		unsigned char val=*ptr;
		int ypos=y+dy;
		if(ypos>=0 && ypos<p.height)
			for(dx=0;dx<8;dx++)
				{
				int xpos=x+8*byte+dx;
				if(val&0x80 && xpos>=0 && xpos<p.width)
					setPixelColor(p,xpos,ypos,col);
				val<<=1;
				}
		ptr++;
		}
	}	
}

void blitString(NCCAPixmap p, char *s, int x, int y, NCCAPixel col)
{
	int i;
	if(y+font.height<0 || y>=p.height)
		return;
	if(x+font.width*((signed int)strlen(s)+1)<0)
		return;

	for(i=0;i<strlen(s);i++)
		{
		if(x+font.width*i>p.width)
			return;

                blitChar(p,s[i],x+font.width*i,y,col);
		}
}

int stringWidth(char *s)
{
	return font.width*(strlen(s));
}

